public class Employee
{
    public int EmployeeId { get; set; }          // Employee ID
    public required string EmployeeName { get; set; }      // Employee Name
    public required string EmployeeEmail { get; set; }     // Employee Email
    public required string EmployeePosition { get; set; }   // Employee Position
    public decimal EmployeeSalary { get; set; }    // Employee Salary
    public required string EmployeeDepartment { get; set; } // Employee Department
}
